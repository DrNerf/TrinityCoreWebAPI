namespace CommandLayer
{
    /// <summary>
    /// World Server Configuration.
    /// </summary>
    public class WorldServerConfiguration
    {
        /// <summary>
        /// Gets or sets the world server address.
        /// </summary>
        public string Address { get; set; }

        /// <summary>
        /// Gets or sets the world server port.
        /// </summary>
        public string Port { get; set; }

        /// <summary>
        /// Gets or sets the world server username.
        /// </summary>
        public string AccountName { get; set; }

        /// <summary>
        /// Gets or sets the world server password.
        /// </summary>
        public string Password { get; set; }
    }
}
